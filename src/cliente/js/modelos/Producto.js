class Producto {
    /**
     * @param {number} id 
     * @param {string} nombre 
     * @param {string} foto 
     * @param {boolean} destacado 
     */
    constructor(id, nombre, foto, destacado) {
        this.id = id;
        this.nombre = nombre;
        this.foto = foto;
        this.destacado = destacado;
    }
}